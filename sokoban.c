/*
 * Find the name of our levelset and return the number of levels in it.
 */
int Levels() {
	i = 0;
	j = 0;
	k = -1;
	l = 0;
	while(leveldata[i]) {
		// readline
		if(leveldata[i++]!='\n') {
			data[j++] = leveldata[i-1];
		} else {
			data[j++] = '\n'; data[j++] = 0; j = 0;

			// if a line contains text, it is the name for k:th levelset
			if((data[0]>='A'&&data[0]<='Z')||(data[0]>='a'&&data[0]<='z')) {
				// levelset of interest ended so return the number of levels
				if(k==set) return l;

				// otherwise record the name of the beginning levelset
				sprintf(levelname, "%s ", data);
				k++;
			}

			// if we're in the set we're looking for, count the number of levels
			if(k==set && data[0]==' ' && data[1]=='\n')
				l++;
		}
	}
	return l;
}

/*
 * Load our level.
 */
int LoadLevel() {
	i = 0;
	j = 0;
	k = -1;
	l = -1;

	while(leveldata[i]) {
		// readline
		if(leveldata[i++]!='\n') {
			data[j++] = leveldata[i-1];
		} else {
			data[j++] = '\n'; data[j++] = 0; j = 0;

			// if a line contains text, it is the name for k:th levelset
			if((data[0]>='A'&&data[0]<='Z')||(data[0]>='a'&&data[0]<='z'))
				k++;
			// if we're in the set we're looking for, start counting the levels
			if(k==set && data[0]==' ' && data[1]=='\n')
				l++;

			// if we're in the set and level we're looking for, collect the level
			if(k==set && l==sets[set]-1) {
				k = i;

				// clean the level
				for(i=0; i<27*20; i++) level[i] = 0;
				i = 0;
				j = 0;

				// if not at the end, keep collecting
				while(!((leveldata[k]=='\n')&&(leveldata[k+1]==' ')&&(leveldata[k+2]=='\n'))) {
					if(leveldata[k++]!='\n')
						level[i+++j*27] = leveldata[k-1];
					else {
						j++; i=0;
					}
				}

				// find the location of our man
				for(j=0; j<20; j++)
					for(i=0; i<27; i++) {
						if(level[i+j*27]=='@') { manx=i; many=j; }
						if(level[i+j*27]=='+') { manx=i; many=j; }
				}

				return 0;
			}
		}
    }
}

/*
 * Draw the level on screen.
 */
int DrawLevel() {
	for(i=0; i<29; i++) {
		DrawIMG(IMG_floor, 16*i, 0);
		DrawIMG(IMG_floor, 16*i, 16*21);
	}
	for(j=0; j<22; j++) {
		DrawIMG(IMG_floor,     0, 16*j);
		DrawIMG(IMG_floor, 16*28, 16*j);
	}
	for(j=0; j<20; j++)
		for(i=0; i<27; i++)
			switch(level[i+j*27]) {
				case '#':
					DrawIMG(IMG_stone, 16*i+16, 16*j+16);
					break;
				case '@':
					DrawIMG(IMG_man, 16*i+16, 16*j+16);
					break;
				case '$':
					DrawIMG(IMG_diamond, 16*i+16, 16*j+16);
					break;
				case '.':
					DrawIMG(IMG_goal, 16*i+16, 16*j+16);
					break;
				case '*':
					DrawIMG(IMG_treasure, 16*i+16, 16*j+16);
					break;
				case '+':
					DrawIMG(IMG_saveman, 16*i+16, 16*j+16);
					break;
				default:
					DrawIMG(IMG_floor, 16*i+16, 16*j+16);
					break;
			}
	SDL_UpdateWindowSurface(window);
}

/*
 * Save the progess to save.txt.
 */
int Save() {
	stream = fopen("sokoban.sav", "w");
	fseek(stream, -8, SEEK_END);
	for(i=0; i<8; i++)
		fputc(sets[i], stream);
	fclose(stream);
}

/*
 * Load the progress from save.txt.
 */
int Load() {
	stream = fopen("sokoban.sav", "r");
	fseek(stream, -8, SEEK_END);
	for(i=0; i<8; i++)
		sets[i] = fgetc(stream);
	LoadLevel();
	levels = Levels();
}

/*
 * Load the level data, draw level and update window title to reflect progress and selected levelset.
 */
int Change() {
	LoadLevel();
	DrawLevel();
	sprintf(fnttext, "%s[%d/%d]", levelname, sets[set]+1, levels = Levels());
	for(i=0; i<256; i++)
		if(fnttext[i]=='\r'||fnttext[i]=='\n')
			fnttext[i] = ' ';
        SDL_SetWindowTitle(window, fnttext);
}

/*
 * Move our character.
 */
int Movement(int x, int y) {
	if(!liikuttiin && level[manx+x+(many+y)*27]=='$' && level[manx+2*x+(many+2*y)*27]==' ') {
		level[manx+x+(many+y)*27] = '@';
		level[manx+2*x+(many+2*y)*27] = '$';
		many += y;
		manx += x;
		liikuttiin = 1;
	}
	if(!liikuttiin && level[manx+x+(many+y)*27]=='$' && level[manx+2*x+(many+2*y)*27]=='.') {
		level[manx+x+(many+y)*27] = '@';
		level[manx+2*x+(many+2*y)*27] = '*';
		many += y;
		manx += x;
		liikuttiin = 1;
	}
	if(!liikuttiin && level[manx+x+(many+y)*27]=='*' && level[manx+2*x+(many+2*y)*27]=='.') {
		level[manx+x+(many+y)*27] = '+';
		level[manx+2*x+(many+2*y)*27] = '*';
		many += y;
		manx += x;
		liikuttiin = 1;
	}
	if(!liikuttiin && level[manx+x+(many+y)*27]=='*' && level[manx+2*x+(many+2*y)*27]==' ') {
		level[manx+x+(many+y)*27] = '+';
		level[manx+2*x+(many+2*y)*27] = '$';
		many += y;
		manx += x;
		liikuttiin = 1;
	}
	if(!liikuttiin && level[manx+x+(many+y)*27]=='.') {
		level[manx+x+(many+y)*27] = '+';
		many += y;
		manx += x;
		liikuttiin = 1;
	}
	if(!liikuttiin && level[manx+x+(many+y)*27]==' ') {
		level[manx+x+(many+y)*27]='@';
		many += y;
		manx += x;
		liikuttiin=1;
	}
	if(liikuttiin) {
		if(level[manx-x+(many-y)*27]=='+') level[manx-x+(many-y)*27] = '.';
		if(level[manx-x+(many-y)*27]=='@') level[manx-x+(many-y)*27] = ' ';
		liikuttiin = 0;
		DrawLevel();
		for(i=0; i<20*27; i++)
			if(level[i]=='.'||level[i]=='+')
				return 0;
		sets[set]++;
		Save();
		Change();
	}
}

